public class operatorsCombinedWithAsign {
    public static void main(String[] args) {
        int number = 10;

        // sum
        number += 5; // equivalent to number = number + 5;

        // substract
        number -= 5;

        // multiply
        number *= 4;

        // division
        number /= 2;

        // rest
        number %= 2;

        System.out.println("i'm the number: " + number);
    }
}
