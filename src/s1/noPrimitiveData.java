public class noPrimitiveData {
    public static void main(String[] args) {
        /*
        Integer ---> numbers + no primitive data + methods of the type of data
        */
        Integer nullData = null;
        System.out.println("i'm the null var: " + nullData);

        /*
        String ---> string of characters + methods of the type of data
        */
        String word = "word";
        System.out.println(word);
    }
}
