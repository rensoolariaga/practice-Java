public class primitiveData {
    public static void main(String[] args) {
        /*
        INTEGER NUMBERS
        byte ----> -128 to 127
        short ---> -32768 a 32767
        int ----> big numbers
        long ---> very big numbers
         */

        byte littleNumber;
        littleNumber = 12;

        System.out.println("i'm the integer number: " + littleNumber);

        /*
        DECIMAL NUMBERS
        float ---> big number
        double ---> very big number
        */

        float decimal = 3.45f;

        System.out.println("I'm the decimal number: " + decimal);


        // char  ---> characters
        String characterVar = "a";
        System.out.println(characterVar);

        boolean riverPlateTheMostBig = true;
        System.out.println("It's River Plate the best team of the world?: " + riverPlateTheMostBig);

    }
}
